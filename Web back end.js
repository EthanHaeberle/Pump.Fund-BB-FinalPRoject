const contract = new ethers.Contract(contractAddress, contractABI, alchemy.core);

async function displayCampaigns() {
  try {
    const [names, ids, descriptions] = await contract.getOpenCampaigns();
    
    const campaignsListDiv = document.getElementById("campaignsList");
    campaignsListDiv.innerHTML = "";  // Clear any existing campaigns
	
    for (let i = 0; i < names.length; i++) {
      const campaignDiv = document.createElement("div");
      campaignDiv.innerHTML = `
        <h3>${names[i]} (#${ids[i]})</h3>
        <p>${descriptions[i]}</p>
      `;
      campaignsListDiv.appendChild(campaignDiv);
    }
  } catch (error) {
    console.error("Error displaying campaigns:", error);
  }
}


 